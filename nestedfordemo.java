package java_in_one_video;

public class nestedfordemo {
    public static void main(String[] args) {

        /*
         * * * * * * * * * * * * *
         */
        for (int i = 1; i <= 4; i++) 
        {
            for (int j = 1; j <= 4; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }

}
